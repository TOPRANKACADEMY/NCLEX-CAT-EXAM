let currentQuestionIndex = 0;
let score = 0;
let startTime = new Date();
let currentDifficulty = 2;  // Starting at a moderate difficulty
let answeredQuestions = [];
let correctAnswersCount = 0;
let totalQuestionsAsked = 0;
let totalDifficultyLevel = 0;
let correctDifficultyLevel = 0;
let timerInterval;
let progressData = {
    labels: [],
    datasets: [{
        label: 'Difficulty Level',
        data: [],
        borderColor: 'rgba(75, 192, 192, 1)',
        borderWidth: 2,
        fill: false
    }]
};

document.addEventListener('DOMContentLoaded', () => {
    const form = document.getElementById('exam-form');
    form.addEventListener('submit', handleFormSubmit);

    document.getElementById('submit-btn').addEventListener('click', handleSubmit);
    document.getElementById('next-btn').addEventListener('click', handleNext);
});

function handleFormSubmit(event) {
    event.preventDefault();
    
    const formData = new FormData(event.target);
    const name = `${formData.get('first-name')} ${formData.get('middle-initial')} ${formData.get('last-name')}`;
    const email = formData.get('email');
    const enrollmentType = formData.get('enrollment-type');
    const examDate = formData.get('exam-date');
    
    document.getElementById('test-taker-name').textContent = name;
    
    console.log(`Name: ${name}`);
    console.log(`Email: ${email}`);
    console.log(`Enrollment Type: ${enrollmentType}`);
    console.log(`Exam Date: ${examDate}`);
    
    document.querySelector('.form-container').style.display = 'none';
    document.getElementById('quiz-container').style.display = 'block';
    document.getElementById('test-taker').style.display = 'block'; // Show name after exam starts
    document.getElementById('timer').style.display = 'block'; // Show timer after exam starts
    document.getElementById('difficulty').style.display = 'block'; // Show difficulty level after exam starts
    
    displayQuestion();
    startTimer();
}

function startTimer() {
    const timerElement = document.getElementById('timer');
    timerInterval = setInterval(() => {
        const now = new Date();
        const elapsed = new Date(now - startTime);
        const minutes = String(elapsed.getUTCMinutes()).padStart(2, '0');
        const seconds = String(elapsed.getUTCSeconds()).padStart(2, '0');
        timerElement.textContent = `Time: ${minutes}:${seconds}`;
    }, 1000);
}

function displayQuestion() {
    const questionContainer = document.getElementById('question-text');
    const optionsContainer = document.getElementById('options-container');
    const feedbackContainer = document.getElementById('feedback-container');
    const submitBtn = document.getElementById('submit-btn');
    const nextBtn = document.getElementById('next-btn');
    const difficulty = document.getElementById('difficulty');

    if (totalQuestionsAsked >= questions.length) {
        showResults();
        return;
    }

    const currentQuestion = selectQuestion();

    questionContainer.innerHTML = `<p>${totalQuestionsAsked + 1}. ${currentQuestion.question}</p>`;

    if (currentQuestion.image) {
        questionContainer.innerHTML += `<img src="${currentQuestion.image}" alt="Question Image" style="max-width: 100%;">`;
    }

    optionsContainer.innerHTML = '';

    if (Array.isArray(currentQuestion.correctAnswer)) {
        // For SATA questions, use checkboxes
        for (const [key, value] of Object.entries(currentQuestion.options)) {
            optionsContainer.innerHTML += `
                <div>
                    <input type="checkbox" id="option${key}" name="option" value="${key}">
                    <label for="option${key}">${key}: ${value}</label>
                </div>
            `;
        }
    } else {
        // For single-answer questions, use radio buttons
        for (const [key, value] of Object.entries(currentQuestion.options)) {
            optionsContainer.innerHTML += `
                <div>
                    <input type="radio" id="option${key}" name="option" value="${key}">
                    <label for="option${key}">${key}: ${value}</label>
                </div>
            `;
        }
    }

    feedbackContainer.style.display = 'none';
    submitBtn.style.display = 'block';
    nextBtn.style.display = 'none';
    difficulty.innerText = `Difficulty Level: ${currentQuestion.difficulty}`;
}

function selectQuestion() {
    let possibleQuestions = questions.filter(q => q.difficulty === currentDifficulty && !answeredQuestions.includes(q));
    if (possibleQuestions.length === 0) {
        possibleQuestions = questions.filter(q => !answeredQuestions.includes(q));  // Fallback to any question
    }
    const selectedQuestion = possibleQuestions[Math.floor(Math.random() * possibleQuestions.length)];
    currentQuestionIndex = questions.indexOf(selectedQuestion);
    answeredQuestions.push(selectedQuestion);
    return selectedQuestion;
}

function handleSubmit() {
    const options = document.getElementsByName('option');
    let selectedOptions = [];

    for (const option of options) {
        if (option.checked) {
            selectedOptions.push(option.value);
        }
    }

    if (selectedOptions.length === 0) {
        alert('Please select an answer.');
        return;
    }

    const currentQuestion = questions[currentQuestionIndex];
    const feedbackContainer = document.getElementById('feedback-container');
    const difficultyLevel = currentQuestion.difficulty;

    totalQuestionsAsked++;
    totalDifficultyLevel += difficultyLevel;

    let correctSelections = 0;
    let incorrectSelections = 0;
    let totalCorrectAnswers = Array.isArray(currentQuestion.correctAnswer) ? currentQuestion.correctAnswer.length : 1;

    if (Array.isArray(currentQuestion.correctAnswer)) {
        for (let option of selectedOptions) {
            if (currentQuestion.correctAnswer.includes(option)) {
                correctSelections++;
            } else {
                incorrectSelections++;
            }
        }
    } else if (selectedOptions[0] === currentQuestion.correctAnswer) {
        correctSelections = 1;
    }

    let pointsEarned = (correctSelections - incorrectSelections) / totalCorrectAnswers;
    if (pointsEarned < 0) {
        pointsEarned = 0;
    }
    correctDifficultyLevel += pointsEarned * difficultyLevel;

    if (pointsEarned === 1) {
        correctAnswersCount++;
        feedbackContainer.innerHTML = `<p>Correct! ${currentQuestion.explanation}</p>`;
        if (currentDifficulty < 5) {
            currentDifficulty = Math.min(currentDifficulty + 1, 5);  // Increase difficulty but cap at level 5
        }
    } else if (pointsEarned > 0 && pointsEarned < 1) {
        feedbackContainer.innerHTML = `<p>Partially correct. ${currentQuestion.explanation}</p>`;
        currentDifficulty = Math.max(currentDifficulty - 1, 1);  // Decrease difficulty by one level
    } else {
        feedbackContainer.innerHTML = `<p>Incorrect. ${currentQuestion.explanation}</p>`;
        currentDifficulty = Math.max(currentDifficulty - 1, 1);  // Decrease difficulty but maintain at level 1
    }

    // Update progress data
    progressData.labels.push(totalQuestionsAsked);
    progressData.datasets[0].data.push(difficultyLevel);

    feedbackContainer.style.display = 'block';
    document.getElementById('submit-btn').style.display = 'none';
    document.getElementById('next-btn').style.display = 'block';

    if (shouldStopQuiz()) {
        displayResults();
    }
}

function handleNext() {
    if (!shouldStopQuiz()) {
        displayQuestion();
    } else {
        displayResults();
    }
}

function shouldStopQuiz() {
    const minQuestions = 85;
    const maxQuestions = 150;
    const passingDifficulty = 3.5;  // Assuming passing if average correct difficulty >= 3.5
    const failingDifficulty = 2.8;  // Failing if average correct difficulty <= 2.8

    if (totalQuestionsAsked >= minQuestions) {
        const averageDifficulty = correctDifficultyLevel / totalQuestionsAsked;
        const passCriteria = averageDifficulty >= passingDifficulty;
        const failCriteria = averageDifficulty <= failingDifficulty;

        return passCriteria || failCriteria || totalQuestionsAsked >= maxQuestions;
    }

    return totalQuestionsAsked >= maxQuestions;
}

function displayResults() {
    clearInterval(timerInterval);
    const quizContainer = document.getElementById('quiz-container');
    const resultContainer = document.getElementById('result-container');
    const totalTime = (new Date() - startTime) / 1000;
    const percentageScore = (correctAnswersCount / totalQuestionsAsked) * 100;
    const averageDifficulty = (correctDifficultyLevel / totalQuestionsAsked).toFixed(2);
    const passStatus = averageDifficulty >= 3.5 ? 'Passed' : 'Failed';
    const resultMessage = passStatus === 'Passed' ? 'CONGRATULATIONS. You are now a USRN.' : 'Study harder to improve your performance. You can do it!';

    quizContainer.style.display = 'none';
    resultContainer.style.display = 'block';

    document.getElementById('summary').innerHTML = `
        <p>Status: ${passStatus}</p>
        <p>Total Questions Answered: ${totalQuestionsAsked}</p>
        <p>Number of Correct Answers: ${correctAnswersCount}</p>
        <p>Number of Incorrect Answers: ${totalQuestionsAsked - correctAnswersCount}</p>
        <p>Percentage Score: ${percentageScore.toFixed(2)}%</p>
        <p>Average Difficulty of Correct Answers: ${averageDifficulty}</p>
        <h2>${resultMessage}</h2>
    `;

    // Create the chart
    const ctx = document.getElementById('progressChart').getContext('2d');
    new Chart(ctx, {
        type: 'line',
        data: progressData,
        options: {
            responsive: true,
            scales: {
                x: {
                    title: {
                        display: true,
                        text: 'Question Number'
                    }
                },
                y: {
                    title: {
                        display: true,
                        text: 'Difficulty Level'
                    },
                    min: 0,
                    max: 5
                }
            }
        }
    });
}
